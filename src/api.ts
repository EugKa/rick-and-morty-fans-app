export async function fetchData(searchBy: string, page: number) {
    try {
        const response = await fetch(`https://rickandmortyapi.com/api${searchBy}?page=${page}`);
        const data = await response.json();
        return data;
    } catch (error) {
        console.log(error);       
    }
}

