export async function parallelCall(urls: string[]) {
    console.log(`urls`, urls)
    try {
        const promises = urls.map((url) =>
            fetch(url).then((response) => response.json())
        );

        const data = await Promise.all(promises);

        return data;
    } catch (error) {
        console.log(error);     
    }
}